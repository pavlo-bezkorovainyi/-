# Програма для перетворення температури з Цельсія у Фаренгейт та Кельвін

celsius = float(input("Введіть температуру у градусах Цельсія: "))

fahrenheit = 32 + 9/5 * celsius
kelvin = celsius + 273.15

print(f"{'Фаренгейт':^15} {'Кельвін':^15}")
print(f"{fahrenheit:^15.2f} {kelvin:^15.2f}")