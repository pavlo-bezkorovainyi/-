# Створюємо англо-німецький словник
e2g = {
    "stork": "storch",
    "hawk": "falke",
    "woodpecker": "specht",
    "owl": "eule"
}

# Додаємо ще два слова до словника
e2g["eagle"] = "adler"
e2g["sparrow"] = "spatz"

# Виводимо весь словник
print("Словник (англо-німецький):")
print(e2g)

# Виводимо лише ключі та значення у вигляді списку
print("\nКлючі словника:")
for key in e2g.keys():
    print(key)

print("\nЗначення словника:")
for value in e2g.values():
    print(value)