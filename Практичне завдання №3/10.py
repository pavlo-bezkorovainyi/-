# Ви створюєте пригодницьку гру і використовуєте для зберігання предметів гравця словник, у якому
# ключі - це назви предметів, значення - кількість одиниць кожної із речей. Наприклад, словник може
# виглядати так: things = {&#39;key&#39;: 3, &#39;mace&#39;: 1, &#39;gold coin&#39;: 24, &#39;lantern&#39;: 1,
# &#39;stone&#39;: 10}. Виведіть повідомлення про усі речі гравця у такому вигляді:

# Створюємо словник з предметами гравця
things = {
    'key': 3,
    'mace': 1,
    'gold coin': 10,
    'lantern': 1,
    'stone': 24
}

# Рахуємо загальну кількість речей
total_things = sum(things.values())

# Виводимо інформацію у потрібному форматі
print("Equipment:")
for item, quantity in things.items():
    print(f"{quantity} {item}")

print(f"Total number of things: {total_things}")