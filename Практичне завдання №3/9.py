# Створюємо словник з командами NBA і їх статистикою
teams = {
    "NEW YORK KNICKS": [22, 7, 6, 9, 45],
    "LOS ANGELES LAKERS": [30, 18, 2, 10, 58],
    "CHICAGO BULLS": [28, 15, 3, 10, 52],
    "BOSTON CELTICS": [25, 14, 1, 10, 49],
    "MIAMI HEAT": [20, 12, 3, 5, 39]
}

# Виводимо інформацію у потрібному форматі
for team, stats in teams.items():
    print(f"{team} {stats[0]} {stats[1]} {stats[2]} {stats[3]} {stats[4]}")